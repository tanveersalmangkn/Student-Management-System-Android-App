package com.example.studentinfo;

public class student_info_model {
    public int student_id;
    public String student_name, student_age, student_registration, student_gender, student_class, student_cellno, student_address, student_img;

    public int getStudent_id() {
        return student_id;
    }

    public void setStudent_id(int student_id) {
        this.student_id = student_id;
    }

    public String getStudent_name() {
        return student_name;
    }

    public void setStudent_name(String student_name) {
        this.student_name = student_name;
    }

    public String getStudent_age() {
        return student_age;
    }

    public void setStudent_age(String student_age) {
        this.student_age = student_age;
    }

    public String getStudent_registration() {
        return student_registration;
    }

    public void setStudent_registration(String student_registration) {
        this.student_registration = student_registration;
    }

    public String getStudent_gender() {
        return student_gender;
    }

    public void setStudent_gender(String student_gender) {
        this.student_gender = student_gender;
    }

    public String getStudent_class() {
        return student_class;
    }

    public void setStudent_class(String student_class) {
        this.student_class = student_class;
    }

    public String getStudent_cellno() {
        return student_cellno;
    }

    public void setStudent_cellno(String student_cellno) {
        this.student_cellno = student_cellno;
    }

    public String getStudent_address() {
        return student_address;
    }

    public void setStudent_address(String student_address) {
        this.student_address = student_address;
    }

    public String getStudent_img() {
        return student_img;
    }

    public void setStudent_img(String student_img) {
        this.student_img = student_img;
    }
}
